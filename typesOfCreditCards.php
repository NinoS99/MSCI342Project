<!--Outline of reward credit cards-->
    <h3> Rewards Credit Cards:</h3>
<!--List of practical reasons-->
    <h5> Practical reasons to use a Rewards credit card: </h5> 
    <ul>
    <h5> <li> "Free rewards": In return for signing up and using the card, you accumulate points or credits to eventually redeem for something of significant value, such as a flight or vacation </li> </h5> 
    <h5> <li> Types of reward programs: Travel credit (offer travel points every time you make a purchase, which can be redeemed for things like plane tickets or hotel rooms), store credit (linked to specific retailers and offer points that can be redeemed for merchandise) etc. </li> </h5> 
<!--List of pros and cons-->
    </ul>
    <h5> Pros of Rewards Credit Cards: <h5>
    <ul>
    <h5> <li> If you’re already making purchases using your credit card, you might as well accumulate rewards along the way </li> </h5>
    <h5> <li> Rewards can help you save money on future purchases </li> </h5>
   </ul>
    <h5> Cons of Rewards Credit Cards: <h5>
    <ul>
    <h5> <li> To accumulate and redeem your rewards, you must be in “good standing”, meaning you are not delinquent in making your minimum monthly payment </li> </h5>
    <h5> <li> Rewards cards tend to carry higher interest rates and are more likely to have annual fees than cards without perks </li> </h5>
    <h5> <li> Some rewards expire after a certain period of time </li> </h5>
    </ul>
     
