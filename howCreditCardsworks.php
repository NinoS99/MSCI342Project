<!--Outline of credit cards consumer basics-->
   <h3> Consumer Credit Card Basics </h3>
   <h5> Credit cards are a common way to pay for almost anything around the world. A person must first submit an application to obtain a card. Once accepted, you'll recieve a card and become the primary cardholder. Each card is assinged a credit limit, the total amount you can charge to it. When you pay for something with a credit card, you are paying with "credit". Any merchant who accepts your credit card recieves payment from the credit card company on your behalf. You are then responsible for paying off the balance of your card. Additionally you can continue to charge your card even after you’ve paid off your previous outstanding balance. For example, if you have a $2,000 credit limit and you owe $500 on it, you can make a $500 payment and then borrow up to the full $2,000 again if you desire. </h5> 
    
<!--Outline of credit cards statement basics-->
    <h3> Credit Card Statement Basics</h3>
    <h5>At the end of your billing cycle, you will recieve a credit card statement that outlines all the purchases and payments you made throughout the month. The two important dates to be aware of on the statement are the statement and payment due date. The statement date is simply the date the statement was created. The payment due date is the date your minimum payment is due. The number of days between the statement date and the payment date is known as the grace period (roughly 21-25 days) and is the time you have to pay off the full balance without having to pay any additional credit card interest charges. For example, let’s say your credit card statement was printed on June 1st, 2020. If the payment due date was June 22nd, 2020, that would mean your card had a 21-day grace period. As long as you paid off the balance in full during the grace period, you will not be charged interest on the balance. However, if you cannot pay off the entire balance, you will be charged interest on the entire balance on your statement (goes back and starts to accrue from the date you made each transaction). </h5>
    
<!--Outline of credit cards payment basics-->
    <h3> Credit Card Payment Basics </h3>
    <h5>Whether or not can you pay the balance in full, you must at least pay the minimum payment each month to avoid interest rate increases or damage. When you make a payment to your credit card, it does not automatically go towards paying down the balance of what you originally spent. It first goes to pay off a portion of your interest charges as well as any other fees included in your statement. Whatever is leftover from the previous months continues to accrue interest, which continually results in a larger balance. This is why you may not see your balance go down quickly enough. However, if you have no outstanding balance, the payment will go straight towards paying off your current statement owing balance.</h5> <br>
    
