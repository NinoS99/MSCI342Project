<!--Outline of credit card annual fees-->
   <h3 class="display-4 text-success"> Annual Fees </h3>
   <h5> Annual fees are a yearly charge for the benefit of using a credit card. Many cards (specifically rewards cards) come with an annual fee. The amount reflects the benefits you will receive (such as travel points and cash back offers) for using the card. Even if you don’t use the card once during the year, this fee still applies. Annual fees typically range from $20-120, but can be more or less. As a credit card’s benefits increase, however, so does its fee. </h5>

<!--Outline of credit card authorized user fees-->
    <h3 class="display-4 text-success"> Authorized User Fees</h3>
    <h5> Authorized User Fees are a yearly charge for each additional credit card you have issued for the same account.This can be a good option for couples to consider, or for a parent to give a kid before they move away for school. However it’s important to remember that the primary cardholder is still responsible for any and all transactions made by the other users. These fees typically range from $20-40 per extra card  </h5> 
    
<!--Outline of credit card overlimit fees-->
    <h3 class="display-4 text-success"> Overlimit Fees </h3>
    <h5> Overlimit Fees is a charge for exceeding your credit limit in a given month. Each credit card has its own credit limit, the maximum amount you can charge to your account. If you make a purchase that pushes you over that limit, you may be subject to an overlimit fee. To avoid this it’s best to use less than 35% of your limit at all times. These fees typically range $25-30. </h5> 
    
<!--Outline of credit card foreign transaction fees-->
    <h3 class="display-4 text-success"> Foreign Transaction Fees</h3>
    <h5> Foreign Transaction Fees are a charge for a purchase made in a different dollar currency in a foreign country. When you make a purchase in a foreign currency, the credit card issuer charges a fee to do the conversion. For example, let’s say you have a credit card with a foreign currency conversion fee of 2.50% of the amount converted. If you were in the US and you made a purchase with your credit card, your card would buy US dollars on your behalf, pay the merchant for the purchase and then charge you for the convenience of doing this conversion. You will see the amount charged to you in your country's currency after the conversion fee has been done. These fees are typically 2.5% of the purchase price. </h5> 

<!--Outline of credit card interest fees-->
    <h3 class="display-4 text-success"> Interest Fees </h3>
    <h5> Interest Fees are a charge imposed when balances are not paid in full by the payment due date. This is the most common and usually largest fee associated with using a credit card. If you do not pay off the balance in full, you start to accrue interest at the annual percentage rate specified in your contract. These fees can range from 0% for introductory offers to as much as 30% if minimum payment has not been met. </h5> 

<!--Outline of credit card adminsitrative fees-->
    <h3 class="display-4 text-success"> Administrative Fees </h3>
    <h5> Administrative Fees are charges for things like copies of statements, dishonoured payments (e.g. bounced cheques) and inactive accounts. Each credit card comes with fees of varying amounts, so there’s no standard, but this information does have to be included in your credit card application. In addition, should the credit card company have to pay legal fees to collect from you, you may be obligated to compensate them for their expenses. It is important that you read the terms of your contract to understand all the potential fees associated with it. These fees vary depending on the situation. Copies of statements can cost up to $10, dishonoured payments can cost up to $40 and inactive accounts may also cost $10.</h5> 
    
