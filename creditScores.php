<!--Outline of credit scores-->
   <h3 class="display-4 text-success"> What is a credit score? </h3>
   <h5> One of the most important reasons to use your credit cards responsibly is to build up your credit score and demonstrate your credit worthiness to lenders. Your credit score is a number between 300 and 900 that tells lenders how risky it is to lend money to you where the higher the score, the lower the risk. </h5>     
        
<!--Outline of how credit score is calculated-->
   <h3 class="display-4 text-success"> How is your credit score calculated? </h3>
   <h5> Credit-reporting agencies use your credit history to calculate your credit score. </h5>

<!--Credit score factors-->
    <h5>Below are some factors that can affect your credit score and weight factors used by FICO, the largest credit-reporting agency in the US: <h5>
    <ul>
    <h5> <li>Your payment history on all accounts (this takes into account if you have been late paying any of your bills) ~35% </li> </h5> 
    <h5> <li>The balances of your open credit relative to their limits ~30% </li> </h5> 
    <h5> <li>How many years you’ve been building your credit history ~15% </li> </h5> 
    <h5> <li>The number of recent inquiries made (e.g. how many times you’ve recently applied for new credit) ~10% </li> </h5> 
    <h5> <li>The types of credit you have (credit cards, loans, etc.) ~10% </li> </h5> 
    </ul>
        
<!--Credit score factors explanation-->
    <h5> Making at least the minimum payment on time is the most important thing you can do to build up a good credit score. The balances on all accounts in relation to your total available credit, is the second most important factor to consider. The length of time you've been building your credit for is another thing credit-reporting agencies look at when determining your credit score. For example, you'll have a better credit score if you use and pay off one card for five years than a card you've had only for a few months. How much “new” credit – including how many times your credit report has recently been pulled – are indicators that you may be having trouble borrowing money and/or borrowing more than you can afford to repay. Finally, it’s better to have multiple types of credit (e.g. a credit card, loan, mortgage, etc.) versus just one type, because it lets lenders know you are responsible handling multiple forms of credit/debt. </h5>        
        
<!--Outline of what is a good credit score -->
   <h3 class="display-4 text-success"> What is considered a "good" credit score? </h3>
   <h5> A perfect score is 900, but anything above 750 is considered excellent. You need a minimum score of around 680 to access the best interest rates on any new forms of credit you take out, including mortgages, loans, lines of credit, etc. If your score is below 680, that doesn’t necessarily mean creditors won’t lend you money but it just means you’ll be subject to higher interest rates since you’ll be seen as a risky borrower, which results in higher risks come and premiums. For example, if your credit score is below 600, you won’t be able to get a mortgage from one of the big banks. Instead, you’ll have to get a bad credit mortgage which comes with much higher interest rates through a bank, private lender or trust company. </h5>    
        
<!--Outline of why a person should have a good credit score -->
   <h3 class="display-4 text-success"> Why do I need a good credit score? </h3>
   <h5> Today credit scores can be used across multiple circumstances including when you’re applying for an apartment rental, an insurance policy and even a new cell phone contract. Some people even have to sign off on future employers running a credit check on them, before they can be hired. Overall, credit scores are meant to prove one thing: how good or “trustworthy” you are with your finances. For this reason, you’ll want to build up and maintain a good credit score for as long as you’ll need access to credit, as the vast majority of people do. </h5> 
        
<!--Outline of how to improve your credit score-->
   <h3 class="display-4 text-success"> How is improve your credit score? </h3>
    <h5>Below are some ways you can use to improve your credit score: <h5>
    <ul>
    <h5> <li>Find out what your credit score is and determine if anything in your credit report is inaccurate. If anything looks wrong, you need to report it to both credit-reporting agencies immediately. Note: Your score is not included in your free credit report, so you will need to pay for it. </li> </h5> 
    <h5> <li>If your credit score is so low that you cannot be approved for a credit card, put a deposit down on a secured or prepaid credit card to start building up your credit again </li> </h5> 
    <h5> <li>Don’t apply for multiple credit cards at the same time. All inquiries related to auto or mortgage loans made within the same 2-4 week period are usually combined and treated as a single inquiry, as the credit bureau recognizes it as “shopping around”. However, the same is not true for credit cards. Registering for several new credit cards over a short period can cause your credit score to drop because it signals distress. Selectively apply for only the best new cards available and space out your applications. </li> </h5> 
    <h5> <li>Pay all of your bills (all forms of credit, as well as utilities, etc.) on time. Even though your regular bills aren’t used to build credit, some cell phone companies may report late payments. </li> </h5> 
    <h5> <li>Try to pay all of your bills in full, or at least pay the minimum </li> </h5> 
    <h5> <li>Never go over the credit limit on your credit cards. Remember: Always try to keep your balance below 35% of the limit. </li> </h5> 
    </ul>
        
